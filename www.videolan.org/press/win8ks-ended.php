<h1>Successful fundraising campaign for VLC ports to Windows 8, RT and Phone</h1>
<br />

<div class="longtext">
<div class="date">Paris, 29 Dec 2012</div>

<p><b>Today, the <a href="http://www.kickstarter.com/projects/1061646928/vlc-for-the-new-windows-8-user-experience-metro" target="_blank">kickstarter campaign</a> run by several core members of the VLC development team ended. £47,056 was pledged by 3080 generous backers. We would like to thank everyone who made this possible!</b></p>

<p>This is a tremendous and somewhat unexpected success. We are very pleased about it.</p>

<p>In the last week, we've prepared our project management tools, determined hardware and software needs for the developers who will work on VLC for Windows 8, RT and Phone, and set up our major development milestones. We will start right away as soon as the campaign is over.<br />
We will perform detailed scheduling early next month and reach out to major IT corporations who offered technical support and testing devices during our campaign.</p>

<p>An initial release of VLC for Windows 8 is estimated to be complete in 3 months, followed by subsequent releases for Windows RT and Windows Phone 8.</p>
<br />
<p><b>VLC media player</b> for Windows has been widely used for a few years now, and it is by far the dominant platform for VLC according to our user base numbers. We estimate an installed base  of about 100,000,000 individual devices on Windows only, used by consumers, professionals, companies and government agencies around the world.</p>
<p>The current version of VLC is able to run on Windows 8. However, it is limited to the desktop experience, hidden by default along with other classic applications behind one of the tiles (interface element of the Start screen). We want to allow users to get access to VLC from the Start screen with the full Metro experience.</p>
<p>To run in Metro, important parts of VLC need to be re-written or ported and an entirely new interface needs to be created. This is very time-consuming, and considering our current Windows design, we need professional designers to adapt the interface to Metro. Additionally, we would like to have it in the hands of our Windows 8 users as soon as possible.</p>
<p>The project funded through this campaign includes support for Windows RT and Windows Phone 8.</p>
<br />
<p><b>Windows 8</b> is the latest release of Microsoft's Windows operating system, for use on personal computers, tablets, and home theater PCs. The operating system was released for general availability on October 26, 2012 and introduced the Modern UI a.k.a. Metro experience known from Windows Phone 7. With <b>Windows RT</b>, this major release adds support for the ARM platform.</p>
<p><b>Windows Phone 8</b> is the latest release of Windows for mobile devices featuring the Modern UI. It was published on October 29, 2012 and includes major improvements over the previous version, which allow us to port VLC to this platform. This is the third major mobile platform supported by VLC following iOS and Android.</p>

