<div class="longtext">
<div class="date">Paris, Jul 18 2013</div>

<p><b>We have exciting news to share. VLC for iOS will be <a href="https://itunes.apple.com/app/id650377962">back on the App Store</a> today!</b></p>

<p>It will be available free of charge in any country, requires iOS 5.1 or later and runs on any iPhone, iPad or iPod touch.</p>

<p>This is more than an upgrade of the initial version: it's a full re-write. It's way faster thanks to modern output modules for audio and video, offers multi-core decoding, and supports any video files available in VLC media player for desktop operating systems.</br />
In addition to the original feature set, we added more ways to synchronize media (upload via WiFi, native Dropbox integration, support for 3rd party apps through the Share dialog, web download), support for network streams, video filters, subtitle rendering, passcode lock and playback speed manipulation.</p>

<p>VLC for iOS is fully open-source. Its code will be available online by tomorrow and is bi-licensed under both the Mozilla Public License Version 2 as well as the GNU General Public License Version 2 or later. The MPLv2 is applicable for distribution on the App Store.<br />
We worked hard with all major copyright holders to not repeat history and we are confident to have achieved this goal.</p>

