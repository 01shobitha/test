<h1>What is DADVSI ?</h1>

<p>The DADVSI (roughly "Author Rights and other Rights in the
Information Society") is the French transcription of the european EUCD
(European Union Copyright Directive) text, which itself comes from the
american DMCA (Digital Millenium Copyright Act).<br />

The main goal of this law is to restrict the rights of digital content
purchasers. It most notably forbids them from working around technical
content protection measures. <br />

Doing so, writing or publishing software allowing to do so, or even
merely talking about ways to do so becomes an offence that can be
punished with three years in jail.</p>

<p> <strong> VideoLAN is directly impacted, most notably for its        
DVD reading capability (all Linux DVD reading software has the same     
problem). Should this law be passed as it is written, this would        
seriously hinder VLC's development. </strong> </p>                      

<p>The french website <a                                                
href="http://eucd.info/index.php?English-readers">eucd.info</a>         
collects a large number of related articles and documentation, and has a 
dedicated english-speaking section.</p> 

<p>Discussion about this law has started in the french National
Assembly, and will resume on January, 17th.<br />

A nice fact is that many MPs, both from the Government's political party
and opposition were very aware of the possible impact of this law on
Free Software, and there was a real debate on the first part of the
law.<br />

The most important parts (mostly about DRM and Peer-To-Peer) will take
place in January, so the battle is not over. <strong>It is thus very
important to keep spreading the word about the dangers of this law.</strong>
</p>

<p>
We would like to thank all the people involved in miscellaneous
information actions, and the MPs that raised concerns concerns about
this text, thus allowing a general public awareness about these issues.
</p>


<h1> Français, ceci vous concerne directement.</h1>

<p>

La loi DADVSI (Droits d'Auteur et Droits Voisins dans la Société de     
l'Information) est la transcription en droit français de la directive   
EUCD (European Union Copyright Directive), qui est elle même inspirée   
du DMCA (Digital Millenium Copyright Act) américain.<br />              

 L'effet principal de ce texte est de restreindre fortement les droits
des particuliers achetant des produits culturels. DADVSI s'oppose ainsi
directement à l'exception de copie privée en autorisant les producteurs
de contenu à mettre en place des mesures techniques l'interdisant, et
surtout en interdisant aux acquéreurs de contourner ces mesures.<br />

Contourner une mesure de protection, écrire ou diffuser un logiciel
permettant de la contourner, ou faire connaître des moyens de le faire
sont assimilés à des délits de contrefaçon, passibles de trois ans de
prison et 300 000 euros d'amende.</p>

<p> <strong> VideoLAN est directement concerné, notamment pour sa       
capacité à lire les DVD (tous les logiciels de lecture de DVD sous      
Linux se retrouveraient illégaux). L'adoption de cette loi aurait un   
effet négatif très fort sur le développement de VLC. </strong> </p>     

<p> <strong>Vous</strong> pouvez aider les nombreuses associations      
(d'artistes, de développeurs, d'enseignants, de consommateurs,          
...) luttant contre cette proposition "liberticide", de <a               
href="http://eucd.info/index.php?2005/11/16/182-appel-defendons-nos-dro 
its-et-libertes">nombreuses façons différentes</a>. </p>                

<p>L'initiative <a href="http://eucd.info">eucd.info</a> rassemble des
informations détaillées sur ce problème et des dossiers complets.</p>

<p>Une première partie du débat a eu lieu du 21 au 23 décembre 2005 à
l'Assemblée Nationale. Au cours de ce débat, de nombreux députés de
tous partis ont mis en avant les dangers de ce texte, notamment pour le
Logiciel Libre.<br />

La suite du débat, notamment les parties concernant directement         
les logiciels libres (articles contre le contournement des mesures      
techniques de protection, pour l'interopérabilité, et sur la            
responsabilité des auteurs de logiciels de transfert), aura lieu à      
partir du 17 Janvier 2006 à l'Assemblée Nationale. <strong>Il importe   
donc de rester mobilisés et de continuer les actions d'information à    
propos des dangers de ce texte.</strong></p>                            

<p>Nous tenons à remercier toutes les personnes participant à
l'information à propos de DADVSI, notamment l'initiative eucd.info.<br
/>

Nous remercions également les nombreux députés ayant attiré l'attention
sur les dangers de ce texte.</p>


