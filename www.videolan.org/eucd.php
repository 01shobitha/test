<h1>What is DADVSI ?</h1>

<p>The DADVSI (roughly "Author Rights and other Rights in the
Information Society") is the French transcription of the European EUCD
(European Union Copyright Directive) text, which itself is very similar
to the American DMCA (Digital Millenium Copyright Act).<br />

The main goal of this law is to restrict the rights of digital content
purchasers. It most notably forbids them to work around technical
content protection measures (oh, by the way, it does not precisely
describe was a technical content protection measure is, neither what is
considered as working around). <br />

Doing so, writing or publishing software allowing to do so, or even
merely talking about ways to do so is illegal with this law.</p>

<p>On March 14th and 15th, this law was passed at the French "Assemblée
Nationale" (House of Representatives).<br />

Despite the promises made by the French Ministry of Culture, this law
does not protect Free Software, nor working around DRM for "legal"
purposes.</p>

<p>Moreover, an amendment was adopted that punishes with 3 years in
jail any person that would write, distribute or promote a software that
is "obviously" aimed at illegal file sharing (except if it is "only"
aimed at legal sharing). This amendment is nicknamed the
<em>Vivendi-Universal Amendment</em>. Guess why...</p>

<p><strong> VideoLAN is directly impacted, most notably for its        
DVD reading capability</strong></p>

<p>This means that VLC will be considered as illegal in France as soon
as this law is officially promulgated. The penalty for distributing or
promoting software that works around DRM is <strong>6 months in jail and
a 30 000 euros fine</strong>.</p>

<p>Here is a non-exhaustive list of free software that are impacted:</p>

<ul>
<li>VLC (reads DVD, works around iTunes DRM)</li>
<li>Mplayer (reads DVD)</li>
<li>Totem (reads DVD)</li>
<li>More generally, any free software DVD player</li>
<li>Bittorent (P2P, could be used for illegal sharing)</li>
<li>eMule and all its variants (P2P, could be used for illegal sharing)</li>
<li>Sharpmusique (works around iTunes DRM)</li>
<li>FairTunes (works around iTunes DRM)</li>
<li>Any software that works around any DRM, either on music files or
others</li>
<li>...</li>
</ul>

