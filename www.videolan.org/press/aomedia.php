<h2>VideoLAN Joins the Alliance for Open Media</h2>

<p><a href="/videolan/">VideoLAN</a>, a non-profit organization that develops and promotes open source multimedia solutions, has joined the <a href="http://aomedia.org/">Alliance for Open Media</a>, a non-profit effort to develop a new <b>open</b> and <b>royalty-free</b> video codec: <b>AV-1</b>. <a href="/">VideoLAN</a> will bring nearly 20 years of experience developing open source multimedia software to help build the next-generation video codec.</p>

<span style="float: right; margin: 10px;"><?php image('partners/aomedia.png', "Alliance for Open Media"); ?></span>

<p>Since its creation, the VideoLAN project has worked to make multimedia simple for everyone, users and professionals. To achieve that goal, VideoLAN has developed several pieces of open source software, including the <a href="/vlc/">VLC media player</a>, the <a href="/developers/x264.html">x264</a> encoder and the <a href="/developers/libdvdcss.html">libdvdcss</a> library. The organization also supports and funds other open source projects, such as; <a href="http://ffmpeg.org/">FFmpeg</a>, <a href="https://libav.org">Libav</a>, and <a href="https://handbrake.fr/">Handbrake</a>.</p>

<p><a href="/">VideoLAN</a> has constantly <b>opposed software patents</b>, as they hinder innovation and increase costs for all but a few. Therefore VideoLAN welcomes the Open Media initiative as an excellent opportunity to create a set of high-quality codecs that seek to remove hidden costs and usage limitations from video streaming.</p>

<p><em>“The Alliance for Open Media is one of the best opportunities we have to finally put multimedia on the right track without having to fear hidden fees or legal threats, or resorting to sub-par quality codecs,”</em> said Jean-Baptiste Kempf, President of the VideoLAN association.</p>

<span style="float: left; margin: 10px;"><?php image('goodies/Cone-Video-large.png', "VLC logo", "", "160px"); ?> </span>
<p>The <a href="http://aomedia.org/">Alliance for Open Media</a> brings together the leading experts in the entire video stack, including our friends from the Xiph project, to work together in pursuit of open, royalty-free and interoperable solutions for the next generation of video delivery.</p>

<p><em>"We're pleased to welcome VideoLAN to the Alliance for Open Media,"</em> said Gabe Frost, Executive Director of the Alliance for Open Media. <em>"Customer expectations for media delivery continue to grow, and fulfilling their expectations requires the concerted energy of the entire ecosystem. We look forward to VideoLAN's contributions to this initiative, which will open the door to a new era of openness and interoperability for internet video."</em></p>


<h2>About the Alliance for Open Media</h2>

<p>The <a href="http://aomedia.org/">Alliance for Open Media</a> is a project of the Joint Development Foundation, an independent non-profit organization that provides the corporate and legal infrastructure to enable groups to establish and operate standards and source code development collaborations.<br />
Launched in 2015, the Alliance for Open Media was formed to define and develop media technologies to address market demand for an open standard for video compression and online delivery.<br />
Its founding members are <b>Amazon, ARM, Cisco, Google, IBM, Intel, Microsoft, Mozilla, Netflix and NVIDIA</b>.</p>
<p>Visit <a href="http://aomedia.org/">http://aomedia.org</a>.</p>

<h2>About VideoLAN</h2>

<p><a href="/">VideoLAN</a> is a non-profit organization, run by volunteers, that supports the development of free and open source multimedia solutions.<br />
Its most famous distributed software, the <a href="/vlc">VLC media player</a>, is completely free and also one of the most used programs in the world. The application is free of advertisements, spyware, bloatware and other misfeatures that could be deceptive to its users.</p>

<p>Visit <a href="http://www.videolan.org/">http://www.videolan.org</a>.</p>

