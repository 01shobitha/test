<h1>Announcing VLC for Apple TV</h1>
<br />

<div class="longtext productDescription">
<div class="date"><b>Paris, Jan 12 2016</b></div>

<p>
The VLC team is proud to announce the first port of VLC on the Apple TV!<br />
<br />
VLC on the Apple TV allows you to get access to all your files and video streams in their native formats without conversions, directly on the new Apple device and your TV.<br />
<br />
Following the VLC goal &quot;<i>VLC plays everything and runs everywhere</i>&quot;, you will get every feature and format support you are used to in VLC, and a few Apple TV specific features.<br />
And, following VideoLAN's mission, VLC on the Apple TV is fully free and open source, and available right now on the App Store.
</p>
<h2>Features summary</h2>
<p>VLC on the Apple TV allows normal playback of all file formats, including chapters navigation, multiple audio and subtitles tracks.<br />
It is also able to play files from UPnP, Windows shares (SMB), FTP and Plex servers; and is able to download automatically subtitles and artwork!<br />
Finally, VLC on the Apple TV allows you a kind of 'casting', where you can send directly your video files for any browser or app to the Apple TV.</p>
<h2>History</h2>
<p>Immediately after Apple announced &quot;<i>the future of television</i>&quot;, we've been working on the SDK to port VLC for iPads and iPhones to this new box.<br />
It needed quite a few adaptations, but it is now finally ready to be published.<br />
As soon as we could, in mid-November, we released it to beta testers, on TestFlight. The huge amount of feedback changed the application quite a bit. Thanks a lot!</p>
<h2>Playback</h2>
<p>In addition to normal playback features, VLC for the Apple TV allows navigation by chapters and multiple audio and subtitles tracks selection.<br />
Unlike any other player on Apple TV so far, VLC supports custom playback speed. VLC is increasingly used to watch talks and documentation in education so it was naturally of high importance for us to include this feature from the start.<br />
<br />
As part of our cross-platform initiative on improving subtitles support and rendering for VLC's forthcoming 3.0 desktop release, VLC on the Apple TV displays any kind of text (srt, SSA, WebVTT, ...) or bitmap subtitles including full support for Right-to-Left languages like Arabic and Hebrew, complex text layout for Malayalam or other Asian languages.<br />
<br />
For web radio and music playback, we integrated with the community service <a href="http://hatchet.is">hatchet.is</a> to show artist imagery and biographies in addition to album artwork.<br />
For the first time, VLC directly integrates with <a href="http://OpenSubtitles.org">OpenSubtitles.org</a>, so missing subtitles can be downloaded by the user during playback.</p>
<h2>Content Discovery &amp; Remote Playback</h2>
<p>VLC for Apple TV integrates discovery of services on the local network, for Windows Shares (SMB), DLNA/UPnP media servers, FTP and PLEX servers.&nbsp;<br />
Login credentials for devices and services can be securely stored on the Apple TV and will be synchronized to the user's iPad or iPhone (and the other way around) to ease access.<br />
<br />
Finally, VLC for Apple TV includes an innovative feature called Remote Playback a custom way of casting media stored on other computers to the TV using a web browser.<br />
<br />
A user can drop one or multiple files on the browser to play those instantly, and the copies will remain available in the device cache locally.<br />
Additionally, this works for pasting directly streams from compatible URLs and multiple actions can be sent to create the current playlist.<br />
And of course, this web interface can be used to control playback.</p>
<h2>Future versions</h2>
<p>We are extremely excited to share this initial version of VLC for Apple TV today and looking forward to deliver further features currently in development, notably native integration with Cloud Services, like Dropbox, OneDrive and Box. Those features will enter beta testing today.<br />
<br />
After concluding 2015 with VLC's first version for Chrome OS, this is a head-start in 2016 with a large number of releases yet to come, notably VLC 3.0 for desktop computers as well as yet another new port!<br />
<br />
We hope that you like our new release as much as we do and we are looking forward to answer any question you might have.</p>
<h2>About VideoLAN</h2>
<p>VideoLAN is a non-profit organization based in Paris, France, with no employees, that supports the development of free and open source multimedia solutions thanks to voluntary contributions from our users.<br />
There is no business model under VLC usage, and VLC is completely free: we don't use any ads, any spyware or other mis-features, that could be deceptive to the users.</p>
